#Clue: Rumus luas adalah panjang * lebar.

panjang_persegi = int(input("Masukkan panjang persegi:"))
lebar_persegi = int(input("Masukkan Lebar persegi:"))
luas_persegi = panjang_persegi * lebar_persegi
print(f"panjang persegi {panjang_persegi} x lebar persegi {lebar_persegi}, = {luas_persegi} luas")
